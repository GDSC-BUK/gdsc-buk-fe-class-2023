// Data Types and Variable Declaration

/**
 * String
 * Anything wrapped around quotation marks
 */

/** created a variable with value `it has been 30 years` */
const text = "it has been 30 years"
console.log(text)

/**
 * Integers/Numbers
 * Integers 0-9
 */

/** created a variable with value `90` */
const age = 90
console.log(age)

/**
 * Arrays/List
 * It is a collection of items (integers, numbers, or mixture of both, or any other data type)
 */

const stringOnlyArray = ['a', 'b', 'c']
console.log(stringOnlyArray)

const numberOnlyArray = [1, 2, 3]
console.log(numberOnlyArray)

const mixedArray = ['a', 2, [1, 2], ['a', 'b']]
console.log(mixedArray)

/**
 * Object/Dictionary/Mapping
 * Consists of a key-value structure
 */

const dictionary = {
    python: "python is a programming language",
    php: "php is a weird language"
}
console.log(dictionary)
